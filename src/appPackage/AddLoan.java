    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        //validations begin
        boolean b1 = true;
        String date1 = "";
        Cal cl1 = new Cal();
        Validation val1 = new Validation();
        try {
            date1 = dchapproved.getDate().toString();
            date1 = cl1.formatedDate(date1);
        } catch (NullPointerException ex) {
            lblEdate.setText("Please Fill");
            b1 = false;
        }
        if ((!val1.isNumeric(txtAmo.getText())) || (txtAmo.getText().equals(""))) {
            lblEamt.setText("Invalid");
            b1 = false;
        }
        if (!val1.isNumeric(TxtDura.getText()) || (txtAmo.getText().equals(""))) {
            lblEdur.setText("Invalid");
            b1 = false;
        }
        if (!val1.isNumeric(txtIntr.getText()) || (txtAmo.getText().equals(""))) {
            lblEintrate.setText("Invalid");
            b1 = false;
        }
        if (!val1.isNumeric(txtNoIns.getText()) || (txtAmo.getText().equals(""))) {
            lblEnoIns.setText("Invalid");
            b1 = false;
        }
        if (!val1.isNumeric(txtMinIns.getText()) || (txtAmo.getText().equals(""))) {
            lblEMinIns.setText("Invalid");
            b1 = false;
        }
        if (!val1.isNumeric(txtAmo.getText()) || (txtAmo.getText().equals(""))) {
            lblEnoIns.setText("Invalid");
            b1 = false;
        }
        if (txtFinanceName.getText().equals("")) {
            lblEfinName.setText("Invalid");
            b1 = false;
        }
        if (txtbranch.getText().equals("")) {
            lblEfinName.setText("Invalid");
            b1 = false;
        }
        if (!val1.isNumeric(txtAccno.getText()) || txtAccno.getText().equals("")) {
            lblEBranchName.setText("Invalid");
            b1 = false;
        }
        if (txtAccno.getText().equals("")) {
            lblEAccno.setText("Invalid");
            b1 = false;
        }
        if (b1) {
            LoanDAO dao1 = new LoanDAO();
            LoanDAO dao2 = new LoanDAO();
            LoanDetails dt1 = new LoanDetails(Double.parseDouble(txtAmo.getText()), date1, TxtDura.getText(),
                    txtdiscript.getText(), Double.parseDouble(txtIntr.getText()), Integer.parseInt(txtAccno.getText()),
                    txtFinanceName.getText(), txtbranch.getText());
            boolean b12 = false;
            if (loanIDToUpdate < 0) {

                b12 = dao1.addTrans(dt1);
                if (b12) {

                    clear();
                    JOptionPane.showMessageDialog(rootPane, "Successfully Added");
                    Profit pf1 = new Profit();
                    pf1.General.setSelectedIndex(1);
                    pf1.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Failed ");
                }

            } else {

                LoanDetails dt2 = new LoanDetails(Double.parseDouble(txtAmo.getText()), date1, TxtDura.getText(),
                        txtdiscript.getText(), Double.parseDouble(txtIntr.getText()), Integer.parseInt(txtAccno.getText()),
                        txtFinanceName.getText(), txtbranch.getText());
                dt2.setNO_of_ins(Integer.parseInt(txtNoIns.getText()));
                System.out.println(txtNoIns.getText() + "a");
                dt2.setMin_ins(Integer.parseInt(txtMinIns.getText()));
                dt2.setID(loanIDToUpdate);

                b12 = dao1.updateLoan(dt2);
                if (b12) {

                    clear();
                    JOptionPane.showMessageDialog(rootPane, "Successfully Updated");
                    Profit pf1 = new Profit();
                    pf1.General.setSelectedIndex(1);
                    pf1.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Failed ");
                }

            }
        }


    }//GEN-LAST:event_btnaddActionPerformed

