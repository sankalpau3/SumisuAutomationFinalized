    public static int loanIDToUpdate = -1;

    public AddLoan(int loandID) {
        loanIDToUpdate = loandID;
        initComponents();

        System.out.println(loanIDToUpdate);
        if (loanIDToUpdate >= 0) {
            btnadd.setText("Update");
            LoanDAO dao1 = new LoanDAO();
            Vector<Vector<String>> dataset = new Vector<Vector<String>>();
            dataset = dao1.getLoanFromID(loanIDToUpdate);
            Vector<String> loanData = dataset.get(0);
            txtAmo.setText(loanData.get(1));
            TxtDura.setText(loanData.get(3));
            txtdiscript.setText(loanData.get(4));
            txtIntr.setText(loanData.get(5));
            txtAccno.setText(loanData.get(6));
            txtFinanceName.setText(loanData.get(7));
            txtbranch.setText(loanData.get(8));
            txtNoIns.setText(loanData.get(11));
            txtMinIns.setText(loanData.get(12));
            java.util.Date date;
            try {
                date = new SimpleDateFormat("yyyy-MM-dd").parse(loanData.get(2));
                dchapproved.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(Profit.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            btnadd.setText("Add");
        }

    }

