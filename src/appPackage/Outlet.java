    private void loadOutletTable(String no, JTable tb1, JScrollPane j1) {
        if (no.equals("Search...")) {
            no = "";
        }
        OutletDAO dao = new OutletDAO();
        data = dao.getoutletDetails(no);
        tblOproduct.setModel(new javax.swing.table.DefaultTableModel(data, header));
        jScrollPane1.setViewportView(tblOproduct);

    }

    private void loadCartTable() {
        OutletDAO dao1 = new OutletDAO();
        dataCart = dao1.getCartDetails();
        tblCart.setModel(new javax.swing.table.DefaultTableModel(dataCart, headerCart));
        jScrollPane2.setViewportView(tblCart);
    }

    //to return outlet cartitems when checkout would not be  happend
    public void clearCart() {
        OutletDAO dao = new OutletDAO();

        while (tblCart.getRowCount() > 0) {

            //public boolean qtyInc(int qty,int id) pass values.return values product tables
            dao.qtyInc(Integer.parseInt((tblCart.getValueAt(0, 3).toString())), Integer.parseInt((tblCart.getValueAt(0, 0).toString())));
            //add rows to table
            DefaultTableModel model = (DefaultTableModel) tblCart.getModel();//cast
            model.removeRow(0);
            loadOutletTable(txtSearch.getText(), tblOproduct, jScrollPane1);

        }
    }

