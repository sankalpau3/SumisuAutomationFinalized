    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        boolean e = true;

        try {
            OutletDAO dao = new OutletDAO();

            int r = tblOproduct.getSelectedRow();

            String id = tblOproduct.getValueAt(r, 0).toString();
            String name = tblOproduct.getValueAt(r, 1).toString();
            String weight = tblOproduct.getValueAt(r, 3).toString();
            String qty = tblOproduct.getValueAt(r, 4).toString();//outlet stock value
            String price = tblOproduct.getValueAt(r, 2).toString();
            int sqty = 0;
            //selected textbox qty
            try {
                if (txtQuantity.getText().equals("")) {
                    lblEQty.setForeground(Color.RED);
                    lblEQty.setText("Enter quentity");
                    e = false;
                } else {
                    sqty = Integer.parseInt(txtQuantity.getText());
                }
            } catch (NullPointerException ex) {
//                lblEQty.setText("Enter quentity");
            } catch (NumberFormatException numberFormatException) {
                e = false;
                JOptionPane.showMessageDialog(null, "Enter only numbers!");
            }
            txtQuantity.setText("");
            int tqty = Integer.parseInt(qty);//outlet stocks qty
            if (e) {
                if (sqty <= tqty) {
                    DefaultTableModel model = (DefaultTableModel) tblCart.getModel();
                    model.addRow(new Object[]{id, name, weight, sqty, price, (sqty * Integer.parseInt(price))});
                    dao.qtyDec(sqty, Integer.parseInt(id));
                    loadOutletTable(txtSearch.getText(), tblOproduct, jScrollPane1);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "The Avaible Quentity is less..." + "Please Reorder");
                }
            }

        } catch (HeadlessException headlessException) {
        } catch (ArrayIndexOutOfBoundsException arrayIndexOutOfBoundsException) {
            System.out.println(arrayIndexOutOfBoundsException);
            JOptionPane.showMessageDialog(rootPane, "Please select a product from the table ...");
        }
    }//GEN-LAST:event_btnAddActionPerformed

